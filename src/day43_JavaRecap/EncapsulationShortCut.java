package day43_JavaRecap;

public class EncapsulationShortCut {
	private int age = 10;
	private int ID =20;
	private int number= 30;
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	
	
			

}
