package day03_Variables;

public class println_vs_print {
	public static void main(String[] args) {
		System.out.println("Hello Cybertek");//prints from a new line
		System.out.println("School");
		System.out.println("===============");
		System.out.print("Hello Cybertek");
		System.out.print("School");//printing takes place from where it`s left off
		System.out.println();
		System.out.println("===============");
		System.out.println("today is");
		System.out.println("great day");
	}

}
